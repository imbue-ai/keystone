#!/usr/bin/env bash
# Label canonical tests for a repo using an interactive Claude session.
#
# Prerequisites:
#   1. Run dump_per_repo_results.py first to populate /tmp/keystone/per_repo_results/
#   2. Install the Claude CLI: https://docs.anthropic.com/en/docs/claude-cli
#
# Usage:
#   ./evals/scripts/label_canonical_tests.sh <repo_id>
#   ./evals/scripts/label_canonical_tests.sh gotham
#   ./evals/scripts/label_canonical_tests.sh scipy
#
# The script launches an interactive Claude session that will analyze the repo's
# test results and produce evals/semi_manual_labels/<repo_id>/canonical_test_names.json

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"

if [ $# -lt 1 ]; then
    echo "Usage: $0 <repo_id>"
    echo ""
    echo "Available repos:"
    ls /tmp/keystone/per_repo_results/ 2>/dev/null || echo "  (none — run dump_per_repo_results.py first)"
    exit 1
fi

REPO_ID="$1"
RESULTS_DIR="/tmp/keystone/per_repo_results/${REPO_ID}"
OUTPUT_DIR="${REPO_ROOT}/evals/semi_manual_labels/${REPO_ID}"

if [ ! -d "$RESULTS_DIR" ]; then
    echo "Error: No results found at ${RESULTS_DIR}"
    echo "Run dump_per_repo_results.py first:"
    echo "  uv run python evals/scripts/dump_per_repo_results.py"
    exit 1
fi

mkdir -p "$OUTPUT_DIR"

echo "=== Labeling canonical tests for: ${REPO_ID} ==="
echo "Results dir: ${RESULTS_DIR}"
echo "Output: ${OUTPUT_DIR}/canonical_test_names.json"
echo ""

# Count available configs/trials
NUM_CONFIGS=$(ls -d "${RESULTS_DIR}"/*/ 2>/dev/null | wc -l | tr -d ' ')
echo "Found ${NUM_CONFIGS} configs with trial data."
echo ""

PROMPT=$(cat <<'PROMPT_EOF'
You are analyzing eval results for the repo "${REPO_ID}" to identify its **canonical test suite** — the set of real, fine-grained tests that a correctly-configured CI environment should be able to pass.

## Your data

All trial results are in: ${RESULTS_DIR}

The directory structure is:
  ${RESULTS_DIR}/<config_name>/trial_<N>/
    passing_tests.txt   — one passing test name per line (sorted)
    Dockerfile          — the Docker image the agent built
    run_all_tests.sh    — the test runner script the agent wrote

Each config_name is a different AI agent/model configuration. Each trial is an independent attempt. Different agents may discover different numbers of tests depending on their setup strategy.

## Your methodology

Follow these steps carefully:

### Step 1: Survey the landscape
- Count how many passing tests each config/trial found (wc -l passing_tests.txt)
- Group configs by test count to identify clusters (e.g., most configs find ~225, one finds ~344)
- Note any extreme outliers (way more or way fewer tests than the majority)

### Step 2: Understand what each agent did differently
- Read the run_all_tests.sh scripts, especially for outliers
- Read the Dockerfiles to understand build strategies
- Identify: Did they use different test runners? Different configs? Different subsets?

### Step 3: Identify fake, inflated, or low-quality test results
Watch for these red flags — do NOT include these as canonical tests:

**Synthetic/fake test names:** Names like "test_0", "test_1", "test_2", etc. that are generated by a script parsing summary counts rather than capturing real test names. These are fabricated.

**Linter-as-tests (RuboCop, ESLint, etc.):** Test names matching patterns like `filename::Category/CopName` where Category contains a `/` (e.g., `Layout/LineLength`, `Style/WordArray`). These are linter rule checks run through a test framework, not real unit/integration tests. They massively inflate test counts (sometimes 10K+ "tests" from a linter).

**Aggregated/summary test names:** Names that represent groups of tests rather than individual ones (e.g., just a test file path without specific test case).

**Double-counted tests:** Some agents parse `cargo test` output and count both unit tests AND doc tests from separate summary lines, inflating the count.

### Step 4: Determine the canonical test set
- Start with the test names from the config(s) that found the MOST tests using a legitimate, fine-grained test reporting approach
- Cross-reference with other configs: if a test appears in multiple independent configs, it's very likely real
- The "canonical" set should represent tests that ANY competent agent setup should be able to pass — the tests that exist in the repo and work in a standard environment

### Step 5: Write regex patterns
For each canonical test (or group of related tests), write a regex that matches it across naming variations. Common variations to handle:
- Trailing "()" on test names (some runners add it, some don't)
- Class/module prefix differences (e.g., "MyClass::test_foo" vs "test_foo")
- Minor path prefix differences
- Parameterized test suffixes

Prefer precise regexes over broad ones. A regex that matches 1-3 specific tests is better than one that might accidentally match junk. Use anchored patterns (^ and $) when practical.

## Output format

Write the result to: ${OUTPUT_DIR}/canonical_test_names.json

The JSON file should have this structure:
```json
{
  "repo_id": "${REPO_ID}",
  "description": "Brief description of the repo and its test suite",
  "methodology_notes": "Brief notes on what you found — which configs were best, any anomalies",
  "total_canonical_tests_approximate": 225,
  "patterns": [
    {
      "regex": "^borrow-bag::append::tests::append_test$",
      "description": "borrow-bag append test",
      "approximate_match_count": 1
    },
    {
      "regex": "^gotham::handler::assets::accepted_encoding::tests::",
      "description": "gotham accepted encoding tests",
      "approximate_match_count": 5
    }
  ]
}
```

Each pattern object has:
- "regex": A Python-compatible regex that matches one or more canonical test names
- "description": Brief human-readable description of what this pattern covers
- "approximate_match_count": Roughly how many test names this pattern should match

## Important guidelines

- **Ask me questions** if anything is ambiguous — e.g., "Should I include doc tests for this Rust project?" or "Config X found 344 tests but they look synthetic — should I use Config Y's 225 instead?"
- **Be skeptical of the highest test counts** — more tests isn't always better if they're fake or linter output
- **Prefer the finest granularity** — individual test cases, not test files or test suites
- **It's okay to have many patterns** — one per test or per small group is fine
- **Validate your patterns**: after writing them, spot-check a few against actual test names from passing_tests.txt to make sure they match
PROMPT_EOF
)

# Substitute variables into the prompt
PROMPT="${PROMPT//\$\{REPO_ID\}/$REPO_ID}"
PROMPT="${PROMPT//\$\{RESULTS_DIR\}/$RESULTS_DIR}"
PROMPT="${PROMPT//\$\{OUTPUT_DIR\}/$OUTPUT_DIR}"

exec claude --allowedTools "Bash,Read,Write,Edit" \
    -p "$PROMPT"
